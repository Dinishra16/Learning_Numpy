# #used to find the stat of a data set
# #1- np.sum
# 2- np.mean
# 3- min(array)
# 4- max(array)
# 5- np.std(array) -- used to find standard deviation
# 6- np.var(arr) -- used to find variance
import numpy as np
arr= np.array([10,20,30,40,50])
print(np.sum(arr))
print(np.mean(arr))
print(min(arr))
print(max(arr))
print(np.std(arr)) 
print(np.var(arr))